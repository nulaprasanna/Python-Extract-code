MERGE INTO SF_ORPHAN_VIEWS_LOG OVL USING (SELECT 1 id, 'x' name from dual) s 
ON (OVL.ACCOUNT_NAME = :ACCOUNT_NAME and OVL.DATABASE_NAME = :DATABASE_NAME 
and OVL.SCHEMA_NAME = :SCHEMA_NAME and OVL.VIEW_NAME = :VIEW_NAME) 
WHEN MATCHED THEN UPDATE SET OVL.ERROR_MSG = :ERROR_MSG , OVL.IS_BAD = :IS_BAD,
OVL.START_TIME = :START_TIME, OVL.END_TIME = :END_TIME
WHEN NOT MATCHED THEN INSERT (ACCOUNT_NAME,DATABASE_NAME,SCHEMA_NAME,VIEW_NAME,ERROR_MSG,IS_BAD, START_TIME, END_TIME) 
VALUES (:ACCOUNT_NAME,:DATABASE_NAME,:SCHEMA_NAME,:VIEW_NAME,:ERROR_MSG,:IS_BAD, :START_TIME, :END_TIME)
