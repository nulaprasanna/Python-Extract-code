MERGE INTO {} T1 USING {} T2 
ON T1.DATABASE_NAME = T2.DATABASE_NAME
AND T1.SCHEMA_NAME = T2.SCHEMA_NAME
AND T1.VIEW_NAME = T2.VIEW_NAME 
WHEN MATCHED THEN UPDATE SET T1.ERROR_MSG = T2.ERROR_MSG, T1.IS_BAD = T2.IS_BAD, 
T1.START_TIME = T2.START_TIME, T1.END_TIME = T2.END_TIME, T1.ROLE = T2.ROLE
WHEN NOT MATCHED THEN INSERT (ACCOUNT_NAME,DATABASE_NAME, SCHEMA_NAME,VIEW_NAME, ERROR_MSG, IS_BAD, START_TIME, END_TIME, ROLE) 
VALUES (T2.ACCOUNT_NAME,T2.DATABASE_NAME, T2.SCHEMA_NAME,T2.VIEW_NAME, T2.ERROR_MSG, T2.IS_BAD, T2.START_TIME, T2.END_TIME, T2.ROLE)