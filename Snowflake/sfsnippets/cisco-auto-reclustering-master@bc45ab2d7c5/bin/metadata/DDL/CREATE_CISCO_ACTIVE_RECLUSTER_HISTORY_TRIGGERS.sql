-- This sequence and trigger ensure a new value is inserted for RUN ID on each run

DROP SEQUENCE CISCO_RECL_REG;

CREATE SEQUENCE CISCO_RECL_REG
  START WITH 1
  MAXVALUE 9999999999999999999999999999
  MINVALUE 1
  NOCYCLE
  NOCACHE
  NOORDER
  NOKEEP
  GLOBAL;

DROP TRIGGER CISCO_RECL_REG_PK_TRIGGER;

CREATE OR REPLACE TRIGGER CISCO_RECL_REG_PK_TRIGGER
BEFORE INSERT
ON CISCO_AUTO_RECLUSTER_HISTORY
FOR EACH ROW
BEGIN
:NEW.RUN_ID:=CISCO_RECL_REG.NEXTVAL;
END;
/
