-- This sequence and trigger ensure a new value is inserted for ID on each run

DROP SEQUENCE CISCO_RECL_REG_PK_SEQ;

CREATE SEQUENCE CISCO_RECL_REG_PK_SEQ
  START WITH 1
  MAXVALUE 9999999999999999999999999999
  MINVALUE 1
  NOCYCLE
  NOCACHE
  NOORDER
  NOKEEP
  GLOBAL;

DROP TRIGGER CISCO_RECL_D_REG_PK_TRIGGER;

CREATE OR REPLACE TRIGGER CISCO_RECL_D_REG_PK_TRIGGER
BEFORE INSERT
ON CISCO_AUTO_RECLUSTER
FOR EACH ROW
BEGIN
:NEW.ID:=CISCO_RECL_REG_PK_SEQ.NEXTVAL;
END;
/
